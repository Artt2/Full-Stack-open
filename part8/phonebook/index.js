const typeDefs = `
  type Book {
    title: String!
    published: Int!
    author: Author!
    genres: [String!]!
    id: ID!
  }

  type Author {
    name: String!
    born: Int
    id: ID!
    bookCount: Int
  }

  type User {
    username: String!
    favoriteGenre: String!
    id: ID!
  }

  type Token {
    value: String!
  }

  type Query {
    bookCount: Int!
    authorCount: Int!
    allBooks: [Book!]!
    allAuthors: [Author!]!
    me: User
  }

  type Query {
    allBooks(author: String): [Book!]!
    allBooks(genre: String): [Book!]!
  }

  type Mutation {
    addBook(
      title: String!
      published: Int!
      author: String!
      genres: [String!]!
    ): Book

    editAuthor(
      name: String!
      setBornTo: Int
    ): Author

    createUser(
      username: String!
      favoriteGenre: String!
    ): User
    
    login(
      username: String!
      password: String!
    ): Token
  }
`

const resolvers = {
  Query: {
    bookCount: async () => {
      const books = await Book.find({}) //all books
      return books.length
    },
    authorCount: async () => {
      const authors = await Author.find({})
      return authors.length
    },
    allBooks: async (root, args) => {
      let query = {}
      if (args.genre) {
        query.genres = { $in: [args.genre] }
      }
      const retBooks = await Book.find(query)
      return retBooks
    },
    allAuthors: async (root, args) => {
      const authors = await Author.find({}) //bookCount not required
      return authors
    }
  },
  Mutation: {
    addBook: async (root, args) => {
      let author = await Author.findOne({ name: args.author })  //try to find Author from database
      if (!author) {  //author not found
        author = new Author({
          name: args.author,
          born: null
        })
        try {
          await author.save() //create new author to db
        } catch (error) {
          throw new GraphQLError("Saving author failed", {
            extensions: {
              code: "BAD_USER_INPUT",
              invalidArgs: args.author,
              error
            }
          })
        }
      }
      const newBook = new Book({
        ...args,
        author: author._id  //save id that references author
      })
      try {
        await newBook.save()
      } catch (error) {
        throw new GraphQLError("Saving book failed", {
          extensions: {
            code: "BAD_USER_INPUT",
            invalidArgs: {...args},
            error
          }
        })
      }

      return newBook
    },
    editAuthor: async (root, args) => {
      const author = await Author.findOne({ name: args.name })
      if (!author) {  //if not found, return null
        return null
      } else if (args.setBornTo) {
        await Author.updateOne({ _id: author._id }, { born: args.setBornTo }) //update
        const updatedAuthor = Author.findOne({ _id: author._id})  //get updated one
        return updatedAuthor
      }
    }
  }
}

const server = new ApolloServer({
  typeDefs,
  resolvers,
})

startStandaloneServer(server, {
  listen: { port: 4000 },
}).then(({ url }) => {
  console.log(`Server ready at ${url}`)
})